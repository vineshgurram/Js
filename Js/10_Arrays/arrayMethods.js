// SOME METHODS IN ARRAY 

// pop() and push() 

let arr = ["a","b","c","d"];
console.log(arr);

// push() --> ADD ITEMS FROM LAST INDEXING

arr.push("e");
arr.push("f");
console.log(arr);

// pop() --> REMOVE ITEMS FROM LAST INDEXING

arr.pop();
arr.pop();
console.log(arr);   

// unshift() and shift()

// unshift() --> ADD ITEMS FROM FIRST INDEXING

arr.unshift("y");
arr.unshift("z");
console.log(arr);

// shift() --> REMOVE ITEMS FROM FIRST INDEXING
// RETURN THE REMOVED ITEMS 
arr.shift();
arr.shift();
console.log(arr);


